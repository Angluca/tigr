int tigrKeyDown(Tigr *bmp, int key)
{
	TigrWin *win;
	int k = tigrWinVK(key);
	if (GetFocus() != bmp->handle)
		return 0;
	win = tigrWin(bmp);
	return win->keys[k] && !win->prev[k];
}

